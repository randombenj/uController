/**
* A standard byte (unsigned char)
* datatype. A byte is 8 bits.
* Can store values from 0 to 256
* There are no negative numbers in a byte (unsigned)
*/

#ifndef BYTE_H_
#define	BYTE_H_

#define	BYTE_MAX	0xFF
#define	BYTE_MIN	0x00

#define	byte	byte_t

typedef	unsigned char	byte_t;

#endif // BYTE_H_
