/**
* The standard boolean datatype
* contains the values true and false
* a boolean uses the 8 bit (unsigned char)
* of storage.
*
* false  = 0 (zero)
* true  >= 1 (1...256)
*/

#ifndef BOOL_H_
#define	BOOL_H_

#define	false	0
#define	true	1

#define	bool	bool_t

typedef	unsigned char	bool_t;

#endif // BOOL_H_
